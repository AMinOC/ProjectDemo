public class BannerImageInfo {
    private String id;
    private String href;
    private String iphone5Img;
    private String iphone6Img;
    private String img;
    private String alt;

    public String getId() {
        return id;
    }

    public String getHref() {
        return href;
    }

    public String getIphone5Img() {
        return iphone5Img;
    }

    public String getIphone6Img() {
        return iphone6Img;
    }

    public String getImg() {
        return img;
    }

    public String getAlt() {
        return alt;
    }

}
